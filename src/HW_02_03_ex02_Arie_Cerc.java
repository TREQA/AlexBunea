/*
Trebuie să creăm un program care calculează aria unui cerc.
 */
public class HW_02_03_ex02_Arie_Cerc {
    static final double PI=3.14;
    public static void main (String[] args)
    {
        double R=5;
        double aria= PI * (R*R);
        System.out.println("Aria cercului cu raza de 5cm este: " + aria + "cm2");
    }
}
